package XMLSAX;

public class PairMovieStar {
    private String first;
    private String second;

    public PairMovieStar(String first,String second)
    {
        this.first = first;
        this.second = second;
    }

    public String getFirst()
    {
        return this.first;
    }

    public String getSecond()
    {
        return this.second;
    }

}
